//Rules for naming var

//(1)You cannot start with number :
//example :-
//1value (invalid)
//value1 (valid)

//(2) You can only use dollar($) or underscore(_) symbol
//first_name (valid)
//_firstName (valid)

//first$name (valid)
//$firstName

// (3)Can not use spaces
//first name (invalid)

//CONVENTION :
//variable name start with small letter and follow camelCase
//ClassName starts with Capital Letter

//snake_case : first_name
//camelCase : firstName

var first_name;
var first$name;
var _firstName;
var $firstName;
var value1 = 10;

//to find power
console.log("Square : " + value1 ** 2);

